    private void tfImgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tfImgMouseClicked
        JFileChooser fc = new JFileChooser();
        int res = fc.showOpenDialog(null);
        if (res == JFileChooser.APPROVE_OPTION) {
            File arquivo = fc.getSelectedFile();
            try {
                p.updateFoto = true;
                p.setImage(ManipulateImage.setImageSize(arquivo.getAbsolutePath(), 251, 251));
                tfImg.setIcon(new ImageIcon(p.getImage()));
            } catch (Exception ex) {
               // System.out.println(ex.printStackTrace().toString());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Voce nao selecionou nenhum arquivo.");
        }
    }//GEN-LAST:event_tfImgMouseClicked

