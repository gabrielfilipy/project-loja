    public void report () throws JRException {
        List<SaleProducts> sales = new ArrayList<>(); 
        SalesProductsController s = new SalesProductsController();
        sales = s.collectionForPDF(Integer.parseInt(tfCode.getText()));
            
        JasperReport relatorioCompilado = JasperCompileManager.compileReport("src/pdf/detailSale.jrxml"); 
        JasperPrint print = JasperFillManager.fillReport(relatorioCompilado, null, new JRBeanCollectionDataSource(sales));
        JDialog screen = new JDialog(this, "Report", true);
        screen.setSize(1000, 600);
        JRViewer j = new JRViewer(print);
        screen.getContentPane().add(j);
        screen.setLocationRelativeTo(null);
        screen.setVisible(true); 
    }

    public void list (int code) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setNumRows(0);
        List<SaleProducts> list = new ArrayList<>();
        SaleProducts p = new SaleProducts();
        SalesProductsController salesProdutcts = new SalesProductsController();
        list = salesProdutcts.collection(code);
        for (int i = 0; i < list.size(); i++) {
            model.addRow(new Object[]{
                list.get(i).getSaleProduct().getId(),
                list.get(i).getProduct().getName(),
                list.get(i).getSaleProduct().getAmount(),
                list.get(i).getSaleProduct().getValue_sale()
            });
        }
    }
    
